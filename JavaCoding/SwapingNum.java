package JavaCoding;

public class SwapingNum {
    public static void main(String[] args){

        int a=3;
        int b=4;
        int t;


        System.out.println("Before swaping number is " +a+ " " +b);

        t=a;
        a=b;
        b=t;

        System.out.println("After swaping number is " +a+ " " +b);





    }
    
}
