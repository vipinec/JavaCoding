package JavaCoding;

public class NaturalNum {

    public static void main(String[] args){


        //Java Program to Find Sum of Natural Numbers

        int n=10;
        int sum =0;

        for(int i=1; i<=n; i++){

            sum=sum+i;
        }
      System.out.println("Sum of natural number is " +sum);



    }
    
}
